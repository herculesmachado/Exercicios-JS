const a = 7 // atribuição 'a' ao valor 7
let b = 3

b += a // b = b + a >> Adição
console.log(b)

b -= 4 // b = b - 4  >> Subtração
console.log(b)

b *= 2 // b = b * 2 >> Multiplicação
console.log(b)

b /= 2 // b = b / 2 >> Divisão
console.log(b)

b %= 2 // b = b % 2 >> resto de divisão
console.log(b )